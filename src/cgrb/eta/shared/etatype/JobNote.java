package cgrb.eta.shared.etatype;

public class JobNote extends ETAType{
	private static final long serialVersionUID = -2219484438856679060L;
	private String note;
	private String date;
	private String user;
	private int userId;
	
	public JobNote(){}
	
	public JobNote(String note,String date,String user,int id,int userId){
		this.note=note;
		this.date=date;
		this.user=user;
		this.id=id;
		this.userId=userId;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}
	
}
